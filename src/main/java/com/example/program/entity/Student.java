package com.example.program.entity;

public class Student {
    Integer st_id;
    String st_name;
    Integer st_gender;
    Integer st_age;
    String st_pro;
    String st_college;
    Integer st_degree;
    String st_email;

    public Integer getSt_id() {
        return st_id;
    }

    public void setSt_id(Integer st_id) {
        this.st_id = st_id;
    }

    public String getSt_name() {
        return st_name;
    }

    public void setSt_name(String st_name) {
        this.st_name = st_name;
    }

    public Integer getSt_gender() {
        return st_gender;
    }

    public void setSt_gender(Integer st_gender) {
        this.st_gender = st_gender;
    }

    public Integer getSt_age() {
        return st_age;
    }

    public void setSt_age(Integer st_age) {
        this.st_age = st_age;
    }

    public String getSt_pro() {
        return st_pro;
    }

    public void setSt_pro(String st_pro) {
        this.st_pro = st_pro;
    }

    public String getSt_college() {
        return st_college;
    }

    public void setSt_college(String st_college) {
        this.st_college = st_college;
    }

    public Integer getSt_degree() {
        return st_degree;
    }

    public void setSt_degree(Integer st_degree) {
        this.st_degree = st_degree;
    }

    public String getSt_email() {
        return st_email;
    }

    public void setSt_email(String st_email) {
        this.st_email = st_email;
    }
}
