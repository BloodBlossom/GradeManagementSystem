package com.example.program.entity;

public class Admin {
    Integer ad_id;
    String ad_name;
    Integer ad_gender;
    String ad_college;
    Integer ad_age;
    String ad_email;

    public Integer getAd_id() {
        return ad_id;
    }

    public void setAd_id(Integer ad_id) {
        this.ad_id = ad_id;
    }

    public String getAd_name() {
        return ad_name;
    }

    public void setAd_name(String ad_name) {
        this.ad_name = ad_name;
    }

    public Integer getAd_gender() {
        return ad_gender;
    }

    public void setAd_gender(Integer ad_gender) {
        this.ad_gender = ad_gender;
    }

    public String getAd_college() {
        return ad_college;
    }

    public void setAd_college(String ad_college) {
        this.ad_college = ad_college;
    }

    public Integer getAd_age() {
        return ad_age;
    }

    public void setAd_age(Integer ad_age) {
        this.ad_age = ad_age;
    }

    public String getAd_email() {
        return ad_email;
    }

    public void setAd_email(String ad_email) {
        this.ad_email = ad_email;
    }
}
